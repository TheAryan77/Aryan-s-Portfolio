  export const calculateSizes = (isSmall, isMobile, isTablet) => {
    const reactLogoPosition = isSmall ? [3, 2, 0] : isMobile ? [5, 4, 0] : isTablet ? [5, 4, 0] : [12, 3, 0];
  
    return {
      astronautposition: [1,-12,-8],
      astronautscale: isSmall? 1.8: 3,
      deskScale: isSmall ? 0.04 : isMobile ? 0.06 : 0.075,
      deskPosition: isMobile ? [0.5, -6, 0] : [0.25, -10, 0],
      badmintonPosition: isSmall ? [-3,-2, 0] : isMobile ? [-9, -6, 0] : isTablet ? [-10, -6, 0] : [-13.25, -10, 0],
      guitarposition:isSmall?[-2,4,0] : isMobile?[-3,3.5,2] : [-12,3.5,2],
      guitarscale: isSmall? 0.8:1.5,
      badmintonScale: isSmall ? 1 : isMobile ? 2.5 : isTablet ? 3 : 3,
      cubePosition: isSmall ? [4, -5, 0] : isMobile ? [5, -5, 0] : isTablet ? [5, -5, 0] : [9, -5.5, 0],
      reactLogoPosition: reactLogoPosition,
      ringPosition: isSmall ? [-5, 7, 0] : isMobile ? [-10, 10, 0] : isTablet ? [-12, 10, 0] : [-24, 10, 0],
      targetPosition: isSmall ? [-5, -10, -10] : isMobile ? [-9, -10, -10] : isTablet ? [-11, -7, -10] : [-13, -13, -10],
      reactscale: isSmall?0.5:1,
  
      // New: Adding chessboard sizes and position below React logo
      chessBoardScale: isSmall ? 0.01 : isMobile ? 0.015 : isTablet ? 0.03 : 0.018,
      chessBoardPosition: isSmall ? [3, -5, 0] : isMobile ? [5, -2, 0] : isTablet ? [5, -3, 0] : [13, -4.5, 0],
  
      // Three.js position is set opposite to React logo
      threejsScale: 0.025,
      threejsPosition: [
        -reactLogoPosition[0],  // Opposite x position
        -reactLogoPosition[1],    // Same y position
        -reactLogoPosition[2]      // Same z position
      ],
    };
  };
  
  
  
  
